    public int pesquisar(String pesquisar) {
        int qtdfuncionario = 0;
        Cfuncionario f = new Cfuncionario();
        DefaultTableModel dt = (DefaultTableModel) tbfuncionario.getModel();
        Object[] recebe = {"CODIGO", "NOME", "GENERO", "ESPECIALIDADE", "Nº DO BI", "TELEFONE",
            "DATA CONTRATO", "ESTADO", "EMAIL", "SENHA", "CARGO"};
        dt.setColumnIdentifiers(recebe);
        dt.setNumRows(0);
        for (Mfuncionario m : f.pesquisar(pesquisar)) {
            Object[] dado = {m.getId_funcionario(), m.getNome(), m.getGenero(), m.getEspecialidade(), m.getN_bi(), m.getTelefone(),
                m.getData_contrato(), m.getStatu(), m.getEmail(), m.getSenha(), m.getCargo()};
            dt.addRow(dado);
            if (m.getStatu().equalsIgnoreCase("activo")) {
                qtdfuncionario++;
            }
        }
        return qtdfuncionario;
    }

