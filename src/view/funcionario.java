    private void tbfuncionarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbfuncionarioMouseClicked

        codigo = (int) tbfuncionario.getValueAt(tbfuncionario.getSelectedRow(), 0);
        //codigo para o duplo clique
        if (evt.getClickCount() == 2) {
            //codigo do Joptiona 
            //declaramos uma vetor objecto que vai armazenar os itens selecionado
            Object[] recebe = {"EDITAR", "CANCELAR"};
            int valor = (int) JOptionPane.showOptionDialog(this, "O QUE DESEJA?", "AVISO",
                    JOptionPane.DEFAULT_OPTION, JOptionPane.PLAIN_MESSAGE, null, recebe, recebe[1]);

            if (valor == 0) {
                
                txtnome.setText((String) tbfuncionario.getValueAt(tbfuncionario.getSelectedRow(), 1));
                cbgenero.setSelectedItem((String) tbfuncionario.getValueAt(tbfuncionario.getSelectedRow(), 2));
                txtespecialidade.setText((String) tbfuncionario.getValueAt(tbfuncionario.getSelectedRow(), 3));
                txtbi.setText((String) tbfuncionario.getValueAt(tbfuncionario.getSelectedRow(), 4));
                txttelefone.setText((String) tbfuncionario.getValueAt(tbfuncionario.getSelectedRow(), 5));
                txtcontrato.setText((String) tbfuncionario.getValueAt(tbfuncionario.getSelectedRow(), 6));
                cbestado.setSelectedItem((String) tbfuncionario.getValueAt(tbfuncionario.getSelectedRow(), 7));
                txtemail.setText((String) tbfuncionario.getValueAt(tbfuncionario.getSelectedRow(), 8));
                txtsenha.setText((String) tbfuncionario.getValueAt(tbfuncionario.getSelectedRow(), 9));
                cbcargo.setSelectedItem((String) tbfuncionario.getValueAt(tbfuncionario.getSelectedRow(), 10));
                btnsalvar.setText("ACTUALIZAR");
                id++;
            }

        }
    }//GEN-LAST:event_tbfuncionarioMouseClicked

